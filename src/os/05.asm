;А теперь ваша задача написать функцию swap. 
;Функция принимает на вход два указателя на 64-битные числа (в регистрах RDI и RSI) и должна обменять значения в памяти.
;
;ВАЖНО: ﻿функция принимает указатели на значения, которые нужно обменять, а не сами значения, 
;т. е. не забудьте, что чтобы добраться до значений указатели нужно разыменовать. 
swap:
    pushq (%RSI)
    pushq (%RDI)
    popq (%RSI)
    popq (%RDI)
    retq